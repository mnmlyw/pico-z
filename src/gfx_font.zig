    data[36] = .{ 0x4E, 0xC2, 0xE4 }; // $ (simplified)
    data[37] = .{ 0xA2, 0x44, 0x8A }; // %
    data[38] = .{ 0x4A, 0x4A, 0x50 }; // &
    data[39] = .{ 0x44, 0x00, 0x00 }; // '
